public class VizVid_entries {

    private String title;
    private String url;
    private String urlForQuest;
    private int playerIndex;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUrlForQuest() {
        return urlForQuest;
    }

    public void setUrlForQuest(String urlForQuest) {
        this.urlForQuest = urlForQuest;
    }

    public int getPlayerIndex() {
        return playerIndex;
    }

    public void setPlayerIndex(int playerIndex) {
        this.playerIndex = playerIndex;
    }

}
