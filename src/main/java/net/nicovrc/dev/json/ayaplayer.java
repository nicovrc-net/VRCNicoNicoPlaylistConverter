package net.nicovrc.dev.json;

public class ayaplayer {

    private int part;
    private int total_parts;
    private int offset;
    private int count;
    private String[] titles;
    private String[] urls;
    private String types_base64;
    private String parents_base64;
    private String targets_base64;

    public int getPart() {
        return part;
    }

    public void setPart(int part) {
        this.part = part;
    }

    public int getTotal_parts() {
        return total_parts;
    }

    public void setTotal_parts(int total_parts) {
        this.total_parts = total_parts;
    }

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String[] getTitles() {
        return titles;
    }

    public void setTitles(String[] titles) {
        this.titles = titles;
    }

    public String[] getUrls() {
        return urls;
    }

    public void setUrls(String[] urls) {
        this.urls = urls;
    }

    public String getTypes_base64() {
        return types_base64;
    }

    public void setTypes_base64(String types_base64) {
        this.types_base64 = types_base64;
    }

    public String getParents_base64() {
        return parents_base64;
    }

    public void setParents_base64(String parents_base64) {
        this.parents_base64 = parents_base64;
    }

    public String getTargets_base64() {
        return targets_base64;
    }

    public void setTargets_base64(String targets_base64) {
        this.targets_base64 = targets_base64;
    }
}
